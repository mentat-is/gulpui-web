interface RgbColor {
	r: number;
	g: number;
	b: number;
}

function parseHexColor(color: string): RgbColor | null {
	const hex = color.replace("#", "").trim();

	if (hex.length === 3) {
		const [r, g, b] = hex.split("");
		return {
			r: parseInt(`${r}${r}`, 16),
			g: parseInt(`${g}${g}`, 16),
			b: parseInt(`${b}${b}`, 16),
		};
	}

	if (hex.length !== 6) {
		return null;
	}

	return {
		r: parseInt(hex.slice(0, 2), 16),
		g: parseInt(hex.slice(2, 4), 16),
		b: parseInt(hex.slice(4, 6), 16),
	};
}

function hueToRgb(p: number, q: number, t: number): number {
	let next = t;

	if (next < 0) next += 1;
	if (next > 1) next -= 1;
	if (next < 1 / 6) return p + (q - p) * 6 * next;
	if (next < 1 / 2) return q;
	if (next < 2 / 3) return p + (q - p) * (2 / 3 - next) * 6;

	return p;
}

function hslToRgb(h: number, s: number, l: number): RgbColor {
	const hue = (((h % 360) + 360) % 360) / 360;
	const saturation = Math.max(0, Math.min(100, s)) / 100;
	const lightness = Math.max(0, Math.min(100, l)) / 100;

	if (saturation === 0) {
		const channel = Math.round(lightness * 255);
		return { r: channel, g: channel, b: channel };
	}

	const q =
		lightness < 0.5
			? lightness * (1 + saturation)
			: lightness + saturation - lightness * saturation;
	const p = 2 * lightness - q;

	return {
		r: Math.round(hueToRgb(p, q, hue + 1 / 3) * 255),
		g: Math.round(hueToRgb(p, q, hue) * 255),
		b: Math.round(hueToRgb(p, q, hue - 1 / 3) * 255),
	};
}

function parseCssColor(color: string): RgbColor | null {
	const value = color.trim();

	if (!value) {
		return null;
	}

	if (value.startsWith("#")) {
		return parseHexColor(value);
	}

	const rgbMatch = value.match(/^rgba?\(([^)]+)\)$/i);
	if (rgbMatch) {
		const [r, g, b] = rgbMatch[1]
			.split(",")
			.slice(0, 3)
			.map((part) => Number.parseFloat(part.trim()));

		if ([r, g, b].some(Number.isNaN)) {
			return null;
		}

		return { r, g, b };
	}

	const hslMatch = value.match(/^hsla?\(([^)]+)\)$/i);
	if (hslMatch) {
		const [h, s, l] = hslMatch[1]
			.split(",")
			.slice(0, 3)
			.map((part) => Number.parseFloat(part.trim().replace("%", "")));

		if ([h, s, l].some(Number.isNaN)) {
			return null;
		}

		return hslToRgb(h, s, l);
	}

	if (
		typeof window !== "undefined" &&
		typeof CSS !== "undefined" &&
		CSS.supports("color", value)
	) {
		const probe = document.createElement("span");
		probe.style.color = value;
		probe.style.display = "none";
		document.body.appendChild(probe);

		const resolved = getComputedStyle(probe).color;
		document.body.removeChild(probe);

		const namedColor = resolved.match(/^rgba?\(([^)]+)\)$/i);
		if (!namedColor) {
			return null;
		}

		const [r, g, b] = namedColor[1]
			.split(",")
			.slice(0, 3)
			.map((part) => Number.parseFloat(part.trim()));

		if ([r, g, b].some(Number.isNaN)) {
			return null;
		}

		return { r, g, b };
	}

	return null;
}

function relativeLuminance({ r, g, b }: RgbColor): number {
	const normalize = (channel: number): number => {
		const value = channel / 255;
		return value <= 0.03928 ? value / 12.92 : ((value + 0.055) / 1.055) ** 2.4;
	};

	return 0.2126 * normalize(r) + 0.7152 * normalize(g) + 0.0722 * normalize(b);
}

