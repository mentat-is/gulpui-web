	export class Entity {
		private static customPaletteRegistry = new Map<string, string[]>();

		private static gradientCache = new Map<
			Gradient,
			Map<number, Map<number, Map<number, string>>>
		>();

		private static clamp = (value: number, min: number, max: number) =>
			Math.min(max, Math.max(min, value));

		private static normalizeHexColor = (value: string) => {
			const normalized = value.trim();
			return /^#[0-9a-f]{6}$/i.test(normalized)
				? normalized.toUpperCase()
				: "#000000";
		};

		private static colorToRgb = (color: string) => {
			const hex = Color.Entity.normalizeHexColor(color).slice(1);
			return {
				r: parseInt(hex.slice(0, 2), 16),
				g: parseInt(hex.slice(2, 4), 16),
				b: parseInt(hex.slice(4, 6), 16),
			};
		};

		private static rgbToHex = (r: number, g: number, b: number) =>
			`#${[r, g, b]
				.map((channel) =>
					Color.Entity.clamp(Math.round(channel), 0, 255)
						.toString(16)
						.padStart(2, "0"),
				)
				.join("")
				.toUpperCase()}`;

		public static normalizePaletteStops = (
			stops: Color.PaletteStop[],
		): Color.PaletteStop[] => {
			const next = [...stops]
				.map((stop) => ({
					color: Color.Entity.normalizeHexColor(stop.color),
					offset: Color.Entity.clamp(Math.round(stop.offset), 0, 100),
				}))
				.sort((a, b) => a.offset - b.offset);

			const first = next.find((stop) => stop.offset === 0) ?? {
				color: "#000000",
				offset: 0,
			};
			const last = next.find((stop) => stop.offset === 100) ?? {
				color: "#FFFFFF",
				offset: 100,
			};
			const middle = next.filter((stop) => stop.offset > 0 && stop.offset < 100);

			return [
				{ ...first, offset: 0 },
				...middle,
				{ ...last, offset: 100 },
			];
		};

		public static colorFromPaletteStops = (
			stops: Color.PaletteStop[],
			offset: number,
		) => {
			const normalizedStops = Color.Entity.normalizePaletteStops(stops);
			const targetOffset = Color.Entity.clamp(offset, 0, 100);
			const upperIndex = normalizedStops.findIndex(
				(stop) => stop.offset >= targetOffset,
			);
			const upper = normalizedStops[Math.max(upperIndex, 0)];
			const lower = normalizedStops[Math.max(upperIndex - 1, 0)] ?? upper;

			if (!upper || lower.offset === upper.offset) {
				return Color.Entity.normalizeHexColor(lower?.color ?? "#000000");
			}

			const factor =
				(targetOffset - lower.offset) / (upper.offset - lower.offset);
			const color1 = Color.Entity.colorToRgb(lower.color);
			const color2 = Color.Entity.colorToRgb(upper.color);

			return Color.Entity.rgbToHex(
				color1.r + (color2.r - color1.r) * factor,
				color1.g + (color2.g - color1.g) * factor,
				color1.b + (color2.b - color1.b) * factor,
			);
		};

		public static buildCustomPalette = (
			stops: Color.PaletteStop[],
			interval: number,
		): string[] => {
			const count = Math.max(1, Math.round(interval));
			return Array.from({ length: count }, (_, index) =>
				Color.Entity.colorFromPaletteStops(
					stops,
					count === 1 ? 0 : (index / (count - 1)) * 100,
				),
			);
		};

		public static registerCustomPalette = (id: string, colors: string[]) => {
			const normalized = colors.map(Color.Entity.normalizeHexColor);
			Color.Entity.customPaletteRegistry.set(id, normalized);
			return normalized;
		};

		public static getCustomPalette = (id: string) =>
			Color.Entity.customPaletteRegistry.get(id);

		public static customPaletteColor = (id: string, value: number) => {
			const palette = Color.Entity.getCustomPalette(id);
			if (!palette?.length) return undefined;
			const index = Math.abs(Math.trunc(value)) % palette.length;
			return palette[index];
		};

		public static gradient = (
			target: Gradient,
			diff: number,
			delta: MinMax,
			overrideLookup?: Record<number, string>,
		): string => {
			const overrideColor = overrideLookup?.[diff];
			if (overrideColor) {
				return overrideColor;
			}

			const normalizedTarget = Color.normalizeGradient(target);
			let targetCache = this.gradientCache.get(normalizedTarget);
			if (!targetCache) {
				targetCache = new Map();
				this.gradientCache.set(normalizedTarget, targetCache);
			}

			let deltaCache = targetCache.get(delta.min);
			if (!deltaCache) {
				deltaCache = new Map();
				targetCache.set(delta.min, deltaCache);
			}

			let maxCache = deltaCache.get(delta.max);
			if (!maxCache) {
				maxCache = new Map();
				deltaCache.set(delta.max, maxCache);
			}

			const value = maxCache.get(diff);
			if (value) {
				return value;
			}

			const gradient = NUMERIC_GRADIENT_MAP[normalizedTarget];
			const numColors = gradient.length;

			const deltaRange = delta.max - delta.min;
			if (deltaRange <= 0) {
				const color = `#${gradient[0].toString(16).padStart(6, "0")}`;
				maxCache.set(diff, color);
				return color;
			}

			const percentage = (diff - delta.min) / deltaRange;
			const scaledIndex = percentage * (numColors - 1);

			const lowerIndex = scaledIndex | 0;
			const upperIndex = Math.min(lowerIndex + 1, numColors - 1);
			const factor = scaledIndex - lowerIndex;

			const color1 = gradient[lowerIndex];
			const color2 = gradient[upperIndex];

			const result =
				(Color.Entity.interpolate(
					(color1 >> 16) & 0xff,
					(color2 >> 16) & 0xff,
					factor,
				) <<
					16) |
				(Color.Entity.interpolate(
					(color1 >> 8) & 0xff,
					(color2 >> 8) & 0xff,
					factor,
				) <<
					8) |
				Color.Entity.interpolate(color1 & 0xff, color2 & 0xff, factor);

			const finalColor = `#${result.toString(16).padStart(6, "0")}`;
			maxCache.set(diff, finalColor);
			return finalColor;
		};

		public static interpolate = (
			c1: number,
			c2: number,
			factor: number,
		): number => ((c1 + factor * (c2 - c1)) & 0xff) | 0;
	}
